// Roman 2026-05-26: продукт #1 — ИИ-ассистент для лички (как сам бот):
// общается, закрывает на продажи, шлёт ссылки/инфо, 24/7. Цель — ПОСТЕПЕННО
// подводить людей к покупке. Цена $1000/5мес (5000 диалогов/мес с 1000 чел).
// Про увеличение тарифа при большем объёме НЕ акцентировать.
//
// Инжектится в промпт когда есть сигнал интереса (ИИ/автоматизация/цена/
// «хочу такого бота») ИЛИ при запросе на партнёра/спеца — чтобы не пушить
// продукт всем подряд, а вплетать естественно.

export interface Product {
    id: string;
    active: boolean;
    name: string;
    description: string;
    price: string;
    included: string;
}

const PRODUCTS: Product[] = [
    {
        id: 'ai-assistant',
        active: true,
        name: 'ИИ-ассистент для лички',
        description: 'ИИ-сотрудник, который сам ведёт переписку в личных сообщениях: общается как живой менеджер, закрывает на продажи, отправляет ссылки и информацию, работает 24/7 без выходных. Снимает рутину с команды и не упускает лиды.',
        price: '$1000 за 5 месяцев',
        included: '5000 диалогов в месяц с 1000 человек',
    },
];

export function getActiveProducts(): Product[] {
    return PRODUCTS.filter(p => p.active);
}

// Сигнал интереса к продукту/продажам/автоматизации → можно вплетать продукт.
const SALES_SIGNAL_RE = new RegExp(
    [
        'сколько\\s+стоит', 'цен[аыоуе]', 'тариф', 'стоимост', 'прайс', 'почём', 'почем',
        'как\\s+(работает|устроен)\\s+\\P{L}*(бот|ассистент|ии|агент)',
        'хочу\\s+\\P{L}*(такого|такой|бота|ассистент)',
        'ваш\\s+\\P{L}*(бот|ассистент|продукт)',
        'ии[\\s-]?ассистент', 'ии[\\s-]?сотрудник', 'ии[\\s-]?агент', 'нейросотрудник',
        'автоматизир\\p{L}*\\s+\\P{L}*(продаж|общени|переписк|личк|лиды|воронк)',
        'внедрить\\s+\\P{L}*(ии|ai|бот)',
    ].join('|'),
    'iu',
);

export function detectSalesSignal(text: string): boolean {
    return !!text && SALES_SIGNAL_RE.test(text);
}

/** Гайд для GPT: вести к продаже ИИ-ассистента ПОСТЕПЕННО. */
export function buildProductSalesPrompt(): string {
    const p = getActiveProducts()[0];
    if (!p) return '';
    return [
        '=== ПРОДУКТ НА ПРОДАЖУ: ИИ-ассистент ===',
        `Один из наших продуктов — ${p.name} (по сути такой же ассистент, как ты): ${p.description}`,
        '',
        'ЦЕЛЬ: ПОСТЕПЕННО подвести человека к покупке этого продукта. НЕ продавай в лоб.',
        'КАК ВЕСТИ:',
        '1. Сначала пойми задачу человека (продажи, поддержка, рутина, упущенные лиды).',
        '2. Покажи на ЕГО примере, как ИИ-ассистент это закроет — конкретно, без воды.',
        '3. Когда человек тёплый / сам спрашивает — предложи продукт и назови цену.',
        `ЦЕНА (называй когда уместно): ${p.price}. Входит: ${p.included}.`,
        'Если спросят про лимиты/больший объём — кратко скажи что тариф расширяется под объём, но НЕ акцентируй и не отпугивай этим. Фокус — на ценности.',
        'Всегда «Вы», нативно, по-человечески. Можно пригласить на эфир — там показывают такого ассистента вживую.',
    ].join('\n');
}
