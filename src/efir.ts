const ACTIVE_EFIR_ID = process.env.ACTIVE_EFIR_ID || '';

export function getActiveEfir(): Efir | null {
    if (ACTIVE_EFIR_ID) {
        const byId = EFIRS.find(e => e.id === ACTIVE_EFIR_ID);
        if (byId) return byId;
    }
    return EFIRS.find(e => e.active) || null;
}

// «эфир» / «ЭФИР» / «эфире» / «на эфир» — кириллич. границы (\b не работает).
const EFIR_RE = /(?:^|[^\p{L}])эфир\p{L}*/iu;
// «анонс» / «пришли анонс» / «полный анонс»
const ANONS_RE = /(?:^|[^\p{L}])анонс\p{L}*/iu;

export function detectEfir(text: string): boolean {
    return !!text && EFIR_RE.test(text);
}

export function detectAnons(text: string): boolean {
    return !!text && ANONS_RE.test(text);
}

/**
 * Промпт-блок для GPT.
 * mode='full'   — попросили анонс → отправить полный анонс ДОСЛОВНО.
 * mode='native' — упомянули эфир → нативно вовлечь и довести до записи.
 */
export function buildEfirPrompt(efir: Efir, mode: 'native' | 'full'): string {
    const dateLine = efir.dateTime ? `Дата/время: ${efir.dateTime}` : 'Дату/время ещё не называй — скажи «пришлю дату».';
    const linkLine = efir.link ? `Ссылка: ${efir.link}` : 'Ссылку ещё не давай — скажи «пришлю ссылку на подключение».';

    if (mode === 'full') {
        return [
            '=== СЦЕНАРИЙ «АНОНС ЭФИРА» (человек попросил анонс) ===',
            'Отправь ПОЛНЫЙ анонс эфира ниже — ДОСЛОВНО, ничего не сокращая и не переписывая.',
            'Можешь разбить на 2-3 сообщения по смысловым блокам (используй ---SPLIT---), но текст сохрани.',
            'После анонса коротко добавь приглашение записаться + спроси про его бизнес для разбора на эфире.',
            dateLine,
            linkLine,
            '',
            '--- АНОНС (дословно) ---',
            efir.fullAnnouncement,
        ].join('\n');
    }

    return [
        '=== СЦЕНАРИЙ «ЭФИР» (человек откликнулся на эфир) ===',
        'Задача: тепло вовлечь и довести до ЗАПИСИ на эфир. НЕ вываливай весь анонс стеной — веди нативно, 1-3 коротких сообщения.',
        'Если человек попросит подробности/анонс целиком — тогда дай полный анонс.',
        '',
        `Тема: ${efir.title}`,
        'О чём (используй 1-2 пункта под человека, не списком-простынёй):',
        ...efir.keyPoints.map(p => `— ${p}`),
        '',
        `Формат: ${efir.format}`,
        `Спикер: ${efir.speaker}`,
        '',
        'КАК ВЕСТИ:',
        '1. Поблагодари за интерес, зацепи ОДНОЙ ценностью под сферу человека.',
        '2. Задай 1 вопрос: какой бизнес / что хочет от ИИ — чтобы разобрать его кейс на эфире.',
        '3. Доведи до записи: зафиксируй, скажи что пришлёшь дату и ссылку.',
        '4. Всегда «Вы», нативно, по-человечески. Не выдумывай дату/ссылку.',
        dateLine,
        linkLine,
    ].join('\n');
}
