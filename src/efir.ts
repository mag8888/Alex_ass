// реагируют. Подача — НАТИВНО, ведём к записи на эфир, без стены текста.
//
// Контент инжектится в GPT-промпт только когда детектор поймал «эфир» —
// чтобы не раздувать токены на каждом сообщении.

// Ловим «эфир» / «ЭФИР» / «эфире» / «на эфир» и т.п. Кириллические границы
// (JS \b не работает с кириллицей — используем класс не-букв / края строки).
const EFIR_RE = /(?:^|[^\p{L}])эфир\p{L}*/iu;

export function detectEfir(text: string): boolean {
    if (!text) return false;
    return EFIR_RE.test(text);
}

