// ── Сценарий «ЭФИР» (data-driven, несколько эфиров) ────────────────────────
// Roman 2026-05-25:
//   1. Разные эфиры с разными спикерами → конфиг-массив, один активный.
//   2. «эфир» → нативно ведём к записи (не стеной).
//   3. «анонс» → присылаем ПОЛНЫЙ анонс активного эфира.
// Оба бота (Артур + Алекс) используют один и тот же активный эфир.
//
// Чтобы добавить новый эфир: добавь объект в EFIRS, поставь active:true ему
// и active:false старому (или поменяй ACTIVE_EFIR_ID). Дату/ссылку заполни
// когда будут.

export interface Efir {
    id: string;
    active: boolean;
    title: string;
    speaker: string;
    /** Полный анонс — отправляется дословно когда просят «анонс». */
    fullAnnouncement: string;
    /** Короткие тезисы для нативного вовлечения (бот берёт 1-2). */
    keyPoints: string[];
    /** Формат проведения. */
    format: string;
    /** Дата/время (МСК). null = ещё не назначено → бот скажет «пришлю дату». */
    dateTime: string | null;
    /** Абсолютное время старта (UTC ISO) — для напоминаний. null = нет. */
    startUTC: string | null;
    /** Ссылка на регистрацию/подключение. null = «пришлю ссылку». */
    link: string | null;
}

