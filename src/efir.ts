const ACTIVE_EFIR_ID = process.env.ACTIVE_EFIR_ID || '';

export function getActiveEfir(): Efir | null {
    if (ACTIVE_EFIR_ID) {
        const byId = EFIRS.find(e => e.id === ACTIVE_EFIR_ID);
        if (byId) return byId;
    }
    return EFIRS.find(e => e.active) || null;
}

// «эфир» / «ЭФИР» / «эфире» / «на эфир» — кириллич. границы (\b не работает).
const EFIR_RE = /(?:^|[^\p{L}])эфир\p{L}*/iu;
// «анонс» / «пришли анонс» / «полный анонс»
const ANONS_RE = /(?:^|[^\p{L}])анонс\p{L}*/iu;

export function detectEfir(text: string): boolean {
    return !!text && EFIR_RE.test(text);
}

export function detectAnons(text: string): boolean {
    return !!text && ANONS_RE.test(text);
}

// Запрос на партнёра/специалиста → пивот на ИИ-внедрение + эфир.
// Roman 2026-05-26: «когда человек говорит что ему нужен партнёр — так же
// предлагать внедрение ИИ и приглашать на эфир».
const PARTNER_NEED_RE = new RegExp(
    [
        'партн[её]р',                                   // партнёр / партнёрство
        'со-?основат',                                  // соучредитель/сооснователь
        '(ищу|ищем|нужен|нужна|нужно|нужны|требуется|подыскива\\p{L}*|хочу\\s+найти)\\s+' +
        '\\P{L}*(маркетолог|продаж|сейлз|трафик|юрист|финанс|бухгалт|таргетолог|смм|spec|спец|сотрудник|помощник|ассистент|менеджер|человек|того)',
        'закро[еюя]\\p{L}*\\s+\\P{L}*(вопрос|задач|маркет|продаж)',  // «закроет вопрос маркетинга»
        'кто\\s+(закро|возьм[её]т|сдела)\\p{L}*\\s+\\P{L}*(вопрос|маркет|продаж)',
    ].join('|'),
    'iu',
);

export function detectPartnerNeed(text: string): boolean {
    return !!text && PARTNER_NEED_RE.test(text);
}

