// ── Context-aware emoji reaction picker ───────────────────────────────────
// Roman: "ставь эмодзи в тему". Подбираем подходящую реакцию исходя из
// содержания сообщения юзера. Все эмодзи — из стандартного набора Telegram
// reactions (иначе REACTION_INVALID).
//
// NB: \b в JS regex работает только с латиницей. Для кириллицы используем
// Unicode-границы (?:^|\P{L}) и (?:$|\P{L}) с флагом /u.

interface Rule {
    emoji: string;
    test: (text: string) => boolean;
}

// helper: word match с поддержкой кириллицы
const word = (re: string) => new RegExp(`(?:^|\\P{L})(?:${re})(?:$|\\P{L})`, 'iu');

// Порядок имеет значение: первое совпадение выигрывает. Сначала яркие
// эмоциональные сигналы, потом данные/факты, в конце — вопросы и fallback.
const RULES: Rule[] = [
    // Благодарность
    { emoji: '🙏', test: t => word('спасибо|благодар(?:ю|ность|им)?|пасиб(?:о)?|thank|thanks|thx').test(t) },

    // Сделка / договорились / по рукам
    { emoji: '🤝', test: t => /по\s+рукам/i.test(t) || word('договорились|погнали|деал|deal').test(t) || /(работаем|сотрудничаем)\s+вместе/i.test(t) },

    // Празднование / готово
    { emoji: '🎉', test: t => word('ура|поздрав(?:ляю|ление|ляем)|готово|закрыли|finished|congrats|launch').test(t) },

    // Огонь / круто
    { emoji: '🔥', test: t => /🔥/.test(t) || word('огонь|пушка|fire|топчик|шикарн(?:о|ый)|круто|улёт|улет|мощно|кайф').test(t) },

    // Сильное короткое "yes" — да/ок/отлично/класс отдельным сообщением
    { emoji: '⚡', test: t => /^\s*(?:да|ок|окей|ok|хорошо|отлично|супер|класс|здорово|то(?:п|пчик)|👍|🙌|💪)\s*[!.]*\s*$/i.test(t) },

    // Любовь / сердце
    { emoji: '❤', test: t => /❤|💕|💛|💖|💜|🧡/.test(t) || word('love|обожаю|люблю').test(t) },

    // Смех
    { emoji: '😁', test: t => /🤣|😂|😆|😅/.test(t) || /ха[\-\s]?ха/i.test(t) || word('хаха|хах|lol|кек|lmao').test(t) },

    // Печаль / извинения
    { emoji: '😢', test: t => word('жаль|увы|грустно|печально|печаль|sorry|извини(?:те)?|прости').test(t) },

    // Удивление
    { emoji: '🤯', test: t => /🤯|😮|🤩/.test(t) || word('ого|вау|wow|охренеть|невероятно|неожиданно').test(t) },

    // Сомнение / непонимание
    {
        emoji: '🤔',
        test: t => /(?:не\s+понял|непонятно|не\s+понятно)/i.test(t)
            || word('поясни|поясните|разъясни|разъясните|уточни|уточните|hmm|хм{2,}').test(t),
    },

    // Внимание / любопытство
    { emoji: '👀', test: t => word('любопытно|интересно|посмотрю|посмотрим|глянем|глянуть|глянул').test(t) },

    // Профильная информация — структурированная подача (Хобби: ..., Город: ..., и т.д.)
    {
        emoji: '✍',
        test: t => /(?:^|\P{L})(?:хобби|интерес[ыа]?|город|сфера|бренд|занятость|роль|локация|клиенты)\s*[:\-—]/iu.test(t)
            || /^@\w+$/i.test(t)
            || /https?:\/\//.test(t),
    },

    // Чистый вопрос (если ничего другого не сработало)
    { emoji: '🤔', test: t => /\?\s*$/.test(t.trim()) },
];

export function pickReaction(text: string): string {
    if (!text) return '👌';
    const t = text.trim();
    for (const r of RULES) {
        if (r.test(t)) return r.emoji;
    }
    return '👌';
}
